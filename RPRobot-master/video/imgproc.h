/* The MJPEG from the webcam device may lack a huffman table. */
extern int jpeg_check_dht(unsigned char* imgbuf, size_t* imgsz);
/* Return 0 for success. */

#define RTP_PT_JPEG 26

struct jpeghdr {
    unsigned int tspec:8;   /* type-specific field */
    unsigned int off:24;    /* fragment byte offset */
    unsigned char type;     /* id of jpeg decoder params */
    unsigned char q;        /* quantization factor (or table id) */
    unsigned char width;    /* frame width in 8 pixel blocks */
    unsigned char height;   /* frame height in 8 pixel blocks */
};

struct jpeghdr_rst {
    unsigned short dri;
    unsigned int f:1;
    unsigned int l:1;
    unsigned int count:14;
};

struct jpeghdr_qtable {
    unsigned char mbz;
    unsigned char precision;
    unsigned short length;
};

#define RTP_JPEG_RESTART 0x40

struct jpeg_frame {
    unsigned char* scan_data;
    int data_size;
    unsigned char* lqt; // quantization table for the luminance channel
    unsigned char* cqt; // quantization table for the chrominance channel
    int qt_size;
    int width, height;
    int restart_interval;
};

